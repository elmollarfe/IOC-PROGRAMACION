public class DocumentacioExample1 {
    public static void main(String[] args) {
        
        //Creacio de la variable que va ser el multiplicador.
        int multiplicador = 5;
        
        
        //Creació del bucle for fins a 10. On multiplicarà el multoplicador * la interació.
        for (int i = 0; i <= 10; i++) {
            
            //Impressió per pantalla del la multiplicació. 
            System.out.println(multiplicador + " * " + i + " = " + (multiplicador * i));
        }
    }
}